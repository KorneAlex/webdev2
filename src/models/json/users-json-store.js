import { v4 } from 'uuid';
import { db } from '../../utils/store-utils.js';

export const usersJsonStore = {
  async getAllUsers() {
    await db.read();
    return db.data.users;
  },

  async addUser(user) {
    await db.read();
    user._id = v4();
    db.data.users.push(user);
    await db.write();
    return user;
  },
};